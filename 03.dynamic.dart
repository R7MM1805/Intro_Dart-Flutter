void main(List<String> args) {
  dynamic myVariable = 'Hola';
  print(myVariable);
  myVariable = 1234;
  print(myVariable);
  myVariable = false;
  print(myVariable);
  myVariable = [1, 2, 3, 4, 5, 6, 7, 8, 9];
  print(myVariable);
  myVariable = {1, 2, 3, 4, 5, 6, 7, 8, 9};
  print(myVariable);
  myVariable = () => true;
  print(myVariable);
  myVariable = null;
  print(myVariable);
}
